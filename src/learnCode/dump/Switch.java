package learnCode.dump;

public class Switch {
    public static void main(String[]args){
        char var = 'f';
        switch(var){
            case 'x':
                System.out.println("hello peeps");
                break;
            case 'y':
                System.out.println("kem cho");
                break;
            case 'z':
                System.out.println("mazaa maadi");
            default:
                System.out.println("Hows the weather");
        }

    }
}
