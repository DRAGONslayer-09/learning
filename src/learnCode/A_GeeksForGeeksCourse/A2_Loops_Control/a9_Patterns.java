package learnCode.A_GeeksForGeeksCourse.A2_Loops_Control;

public class a9_Patterns {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            for (int j = 1; j<=5; j ++){
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
